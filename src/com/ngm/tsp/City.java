package com.ngm.tsp;

class City {
    private int cX = 0; //X-coordinate of a city
    private int cY = 0; //Y-coordinate of a city

    public int getcX() {
        return cX;
    }

    public void setcX(final int xCoordinate) {
        this.cX = xCoordinate;
    }

    public int getcY() {
        return cY;
    }

    public void setcY(final int yCoordinate) {
        this.cY = yCoordinate;
    }
}
